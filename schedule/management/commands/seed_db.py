    #  9. ОБМЕЖЕННЯ ДОСТУПНОСТІ ВЧИТЕЛІВ                                  #
    # ------------------------------------------------------------------ #
    def _seed_availability(self, teachers, slots):
        """
        Реалістичні обмеження: вчителі недоступні в конкретні слоти
        через секції, курси або особисті обставини.
        """

        # Отримуємо слоти зручно за ключем (день, номер)
        slot_map = {
            (s.day_of_week, s.lesson_number): s
            for s in slots
        }

        def get_slot(day, num):
            return slot_map.get((day, num))

        unavailable = [
            # Гриценко (фізика): веде гурток у пт після обіду
            (teachers['Гриценко'], get_slot('FRI', 6), 'Керівництво фізичним гуртком'),
            (teachers['Гриценко'], get_slot('FRI', 7), 'Керівництво фізичним гуртком'),

            # Лисенко (інформатика): курси підвищення кваліфікації пн ранок
            (teachers['Лисенко'],  get_slot('MON', 1), 'Курси підвищення кваліфікації'),
            (teachers['Лисенко'],  get_slot('MON', 2), 'Курси підвищення кваліфікації'),

            # Моисеєнко (фізкультура): тренування збірної сер після обіду
            (teachers['Моисеєнко'], get_slot('WED', 6), 'Тренування шкільної збірної'),
            (teachers['Моисеєнко'], get_slot('WED', 7), 'Тренування шкільної збірної'),

            # Харченко (англійська): методоб'єднання чт 5-й урок
            (teachers['Харченко'], get_slot('THU', 5), 'Засідання методичного об\'єднання'),

            # Романенко (хімія): лабораторна підготовка вт 1-й урок
            (teachers['Романенко'], get_slot('TUE', 1), 'Підготовка обладнання для лабораторних'),
        ]

        count = 0
        for teacher, slot, reason in unavailable:
            if slot is None:
                continue
            _, created = TeacherAvailability.objects.get_or_create(
                teacher=teacher,
                time_slot=slot,
                defaults={'status': 'UNAVAILABLE'},
            )
            if created:
                count += 1
                self.stdout.write(
                    f'    {teacher.last_name}: недоступний '
                    f'{slot.get_day_of_week_display()}, урок {slot.lesson_number}'
                    f' ({reason})'
                )

        self.stdout.write(f'  Обмежень       : {count} записів TeacherAvailability')