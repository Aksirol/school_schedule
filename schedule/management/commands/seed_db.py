class Command(BaseCommand):
    help = 'Заповнює БД реалістичними даними школи для демонстрації системи'

    def add_arguments(self, parser):
        parser.add_argument(
            '--clear',
            action='store_true',
            help='Очистити всі таблиці перед заповненням (крім міграцій)',
        )

    def handle(self, *args, **options):
        if options['clear']:
            self._clear_db()

        year      = self._seed_academic_year()
        slots     = self._seed_time_slots()
        subjects  = self._seed_subjects()
        rooms     = self._seed_rooms()
        classes   = self._seed_classes(year)
        teachers  = self._seed_teachers()
        self._seed_users(teachers)
        self._seed_curriculum(classes, subjects, teachers, year)
        self._seed_availability(teachers, slots)

        self.stdout.write('')
        self.stdout.write(self.style.SUCCESS('=' * 55))
        self.stdout.write(self.style.SUCCESS('  Seed завершено. Дані школи завантажено успішно.'))
        self.stdout.write(self.style.SUCCESS('=' * 55))
        self.stdout.write(f'  Навчальний рік : {year}')
        self.stdout.write(f'  Часових слотів : {len(slots)}')
        self.stdout.write(f'  Предметів      : {len(subjects)}')
        self.stdout.write(f'  Кабінетів      : {len(rooms)}')
        self.stdout.write(f'  Класів         : {len(classes)}')
        self.stdout.write(f'  Вчителів       : {len(teachers)}')
        self.stdout.write('')
        self.stdout.write('  Облікові записи для входу:')
        self.stdout.write('    admin   / admin1234   (Адміністратор)')
        self.stdout.write('    deputy  / deputy1234  (Заступник директора)')
        self.stdout.write('    ivanova / teacher1234 (Вчитель — Іванова О.М.)')
        self.stdout.write(self.style.SUCCESS('=' * 55))

    # ------------------------------------------------------------------ #
    #  ОЧИЩЕННЯ                                                            #
    # ------------------------------------------------------------------ #
    def _clear_db(self):
        self.stdout.write(self.style.WARNING('Очищення таблиць...'))
        TeacherAvailability.objects.all().delete()
        Curriculum.objects.all().delete()
        User.objects.filter(is_superuser=False).delete()
        Teacher.objects.all().delete()
        SchoolClass.objects.all().delete()
        Room.objects.all().delete()
        Subject.objects.all().delete()
        TimeSlot.objects.all().delete()
        AcademicYear.objects.all().delete()
        self.stdout.write(self.style.WARNING('  Таблиці очищено.\n'))

    # ------------------------------------------------------------------ #
    #  1. НАВЧАЛЬНИЙ РІК                                                   #
    # ------------------------------------------------------------------ #
    def _seed_academic_year(self):
        year, created = AcademicYear.objects.get_or_create(
            name='2025-2026',
            defaults={
                'start_date': date(2025, 9, 1),
                'end_date':   date(2026, 5, 29),
            }
        )
        status = 'створено' if created else 'вже існує'
        self.stdout.write(f'  Навчальний рік [{status}]: {year}')
        return year

    # ------------------------------------------------------------------ #
    #  2. ЧАСОВІ СЛОТИ  (7 уроків × пн–пт)                                #
    # ------------------------------------------------------------------ #
    def _seed_time_slots(self):
        # Реальний розклад дзвінків (45-хв. уроки, перерви 10/20 хв.)
        bell_schedule = [
            (1, time(8, 00), time(8, 45)),
            (2, time(8, 55), time(9, 40)),
            (3, time(10, 00), time(10, 45)),   # велика перерва 20 хв.
            (4, time(10, 55), time(11, 40)),
            (5, time(12, 00), time(12, 45)),   # обідня перерва 20 хв.
            (6, time(12, 55), time(13, 40)),
            (7, time(13, 50), time(14, 35)),
        ]
        days = ['MON', 'TUE', 'WED', 'THU', 'FRI']

        created_slots = []
        for day in days:
            for num, start, end in bell_schedule:
                slot, _ = TimeSlot.objects.get_or_create(
                    day_of_week=day,
                    lesson_number=num,
                    defaults={'start_time': start, 'end_time': end},
                )
                created_slots.append(slot)

        self.stdout.write(f'  Часових слотів : {len(created_slots)} (7 уроків × 5 днів)')
        return created_slots

